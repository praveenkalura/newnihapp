def _detect_gdal() -> bool:
    try:
        from django.contrib.gis import gdal  # noqa: F401
        return True
    except Exception:
        return False

_use_gis_env = os.environ.get('USE_GIS')
USE_GIS = _detect_gdal() if _use_gis_env is None else _use_gis_env.lower() in ('1', 'true', 'yes', 'on')

